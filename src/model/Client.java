package model;

/**
 *
 * @author Admin
 */
public class Client {
    private int status;
    private Room room;
    private User user;

    public Client() {
    }

    public Client(int status, Room room, User user) {
        this.status = status;
        this.room = room;
        this.user = user;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public Room getRoom() {
        return room;
    }

    public void setRoom(Room room) {
        this.room = room;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
    
}
